package org.nerdcoding.example.ssia.ch6.service;


import org.nerdcoding.example.ssia.ch6.service.model.CustomerUserDateils;
import org.nerdcoding.example.ssia.ch6.persistence.entity.User;
import org.nerdcoding.example.ssia.ch6.persistence.repository.UserRepository;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

@Service
public class JpaUserDetailsService implements UserDetailsService {

    private final UserRepository userRepository;

    @Autowired
    public JpaUserDetailsService(final UserRepository userRepository) {
        this.userRepository = userRepository;
    }

    @Override
    public UserDetails loadUserByUsername(final String username) throws UsernameNotFoundException {
        final User user = userRepository
                .findUserByUsername(username)
                .orElseThrow(() -> new UsernameNotFoundException(
                        String.format("User with name %s was not found.", username)
                ));

        return new CustomerUserDateils(user);
    }
}
